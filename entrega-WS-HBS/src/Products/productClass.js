class Product {
    constructor(title, description, code, price, status, stock, category, id) {
        this.title = title;
        this.description = description;
        this.code = code;
        this.price = price;
        this.status = status;
        this.stock = stock;
        this.category = category;       
        this.id = id;
    }
    
}

export default Product